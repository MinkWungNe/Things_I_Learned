//Calculate sum d from 3 integers a,b,c - Tính tổng d từ 3 số nguyên a,b,c
#include <iostream>
using namespace std;

int main()
{
    int a, b, c, d;
    cout << "Input a: "; cin >> a;
    cout << "Input b: "; cin >> b;
    cout << "Input c: "; cin >> c;
    d = a + b +c;
    cout << "Sum d: " << d << endl;
    return 0;
}
