# Nhập dữ liệu
a = float(input("Nhập số a: "))
b = float(input("Nhập số b: "))
# Thực hiện các phép so sánh
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)
print("a >= b:", a >= b)
print("a <= b:", a <= b)

# Here I learned how to perform basic comparison operations in Python from user input.